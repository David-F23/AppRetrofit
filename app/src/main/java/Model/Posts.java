package Model;

public class Posts {

    private int userId;
    private int id;
    private String tittle;
    private String body;

    public int getUserId() {
        return userId;
    }

    public int getId() {
        return id;
    }

    public String getTittle() {
        return tittle;
    }

    public String getBody() {
        return body;
    }
}
